// Map service icon names to components
export function ServiceIcon({ icon, className }: { icon: string; className?: string }) {
  switch (icon) {
    case 'booking': return <CalendarCheckIcon className={className} />
    case 'rooms': return <BedIcon className={className} />
    case 'rates': return <TagIcon className={className} />
    case 'channels': return <GlobeIcon className={className} />
    case 'payments': return <CreditCardIcon className={className} />
    case 'analytics': return <BarChartIcon className={className} />
    default: return <HotelIcon className={className} />
  }
}

